async function syncAutoGeneratedCandidates(
  supabaseAdmin: ReturnType<typeof createClient>,
  intake: MedicalDocumentIntakeRow,
  evidenceSegments: MedicalDocumentEvidenceSegmentRow[],
): Promise<CandidateGenerationSummary> {
  const { data: existingCandidates, error: candidateError } = await supabaseAdmin
    .from("candidate_medical_facts")
    .select("*")
    .eq("user_id", intake.user_id);

  if (candidateError) throw candidateError;

  const { data: activeFacts, error: activeFactError } = await supabaseAdmin
    .from("medical_facts")
    .select("id, user_id, category, confirmation_state, detail, is_active, created_at, updated_at")
    .eq("user_id", intake.user_id)
    .eq("is_active", true)
    .in("confirmation_state", ["confirmed", "user_reported"]);

  if (activeFactError) throw activeFactError;

  const allCandidateRows = (existingCandidates ?? []) as CandidateMedicalFactRow[];
  const existingRows = allCandidateRows.filter(
    (candidate) => candidate.source_document_id === intake.id,
  );
  const sameDocumentSignatures = new Set(
    existingRows.map((candidate) => buildCandidateSignature(candidate.category, candidate.detail)),
  );
  const otherPendingCandidates = allCandidateRows.filter(
    (candidate) =>
      candidate.review_status === "pending_review" &&
      candidate.source_document_id !== intake.id,
  );
  const activeFactRows = (activeFacts ?? []) as MedicalFactRow[];

  const pendingBySignature = new Map<string, CandidateMedicalFactRow>();
  const pendingByMergeKey = new Map<string, CandidateMedicalFactRow[]>();
  for (const candidate of otherPendingCandidates) {
    const signature = buildCandidateSignature(candidate.category, candidate.detail);
    if (!pendingBySignature.has(signature)) {
      pendingBySignature.set(signature, candidate);
    }

    addToLookupMap(
      pendingByMergeKey,
      buildCandidateMergeKey(candidate.category, candidate.detail),
      candidate,
    );
  }

  const activeFactBySignature = new Map<string, MedicalFactRow>();
  const activeFactByMergeKey = new Map<string, MedicalFactRow[]>();
  for (const fact of activeFactRows) {
    const signature = buildCandidateSignature(fact.category, fact.detail);
    if (!activeFactBySignature.has(signature)) {
      activeFactBySignature.set(signature, fact);
    }

    addToLookupMap(
      activeFactByMergeKey,
      buildCandidateMergeKey(fact.category, fact.detail),
      fact,
    );
  }

  const generatedBySignature = new Map<string, AutoGeneratedCandidate>();

  for (const segment of evidenceSegments) {
    for (const candidate of buildCandidatesFromEvidenceSegment(segment)) {
      const signature = buildCandidateSignature(candidate.category, candidate.detail);
      if (sameDocumentSignatures.has(signature)) continue;

      const current = generatedBySignature.get(signature);
      if (!current || candidate.extractionConfidence > current.extractionConfidence) {
        generatedBySignature.set(signature, candidate);
      }
    }
  }

  let pendingCreatedCount = 0;
  let mergedCreatedCount = 0;
  let conflictFlaggedCount = 0;

  for (const candidate of generatedBySignature.values()) {
    const signature = buildCandidateSignature(candidate.category, candidate.detail);
    const mergeKey = buildCandidateMergeKey(candidate.category, candidate.detail);
    const exactActiveFact = activeFactBySignature.get(signature) ?? null;
    const exactPendingCandidate = pendingBySignature.get(signature) ?? null;
    const overlappingActiveFact =
      mergeKey
        ? (activeFactByMergeKey.get(mergeKey) ?? []).find(
            (fact) => buildCandidateSignature(fact.category, fact.detail) !== signature,
          ) ?? null
        : null;
    const overlappingPendingCandidate =
      mergeKey
        ? (pendingByMergeKey.get(mergeKey) ?? []).find(
            (row) => buildCandidateSignature(row.category, row.detail) !== signature,
          ) ?? null
        : null;

    let reviewStatus: CandidateMedicalFactRow["review_status"] = "pending_review";
    let reviewedAt: string | null = null;
    let promotedFactId: string | null = null;
    let extractionNotes = candidate.extractionNotes;

    if (exactActiveFact) {
      reviewStatus = "merged";
      reviewedAt = new Date().toISOString();
      promotedFactId = exactActiveFact.id;
      extractionNotes = appendStructuredCandidateNote(
        extractionNotes,
        AUTO_MERGE_NOTE_PREFIX,
        createMergeResolutionNote(candidate.category, candidate.detail, "active_fact"),
      );
    } else if (exactPendingCandidate) {
      reviewStatus = "merged";
      reviewedAt = new Date().toISOString();
      extractionNotes = appendStructuredCandidateNote(
        extractionNotes,
        AUTO_MERGE_NOTE_PREFIX,
        createMergeResolutionNote(candidate.category, candidate.detail, "pending_candidate"),
      );
    } else if (overlappingActiveFact) {
      extractionNotes = appendStructuredCandidateNote(
        extractionNotes,
        AUTO_CONFLICT_NOTE_PREFIX,
        createConflictResolutionNote(candidate.category, candidate.detail, "active_fact"),
      );
      conflictFlaggedCount += 1;
    } else if (overlappingPendingCandidate) {
      extractionNotes = appendStructuredCandidateNote(
        extractionNotes,
        AUTO_CONFLICT_NOTE_PREFIX,
        createConflictResolutionNote(candidate.category, candidate.detail, "pending_candidate"),
      );
      conflictFlaggedCount += 1;
    }

    const { data: insertedCandidate, error: insertCandidateError } = await supabaseAdmin
      .from("candidate_medical_facts")
      .insert({
        user_id: intake.user_id,
        category: candidate.category,
        detail: candidate.detail,
        extraction_source: "document_extraction",
        source_document_id: intake.id,
        extraction_confidence: candidate.extractionConfidence,
        extraction_notes: extractionNotes,
        review_status: reviewStatus,
        reviewed_at: reviewedAt,
        promoted_fact_id: promotedFactId,
      })
      .select("id")
      .maybeSingle();

    if (insertCandidateError) throw insertCandidateError;
    if (!insertedCandidate || typeof insertedCandidate !== "object" || !("id" in insertedCandidate)) {
      continue;
    }

    const { error: evidenceError } = await supabaseAdmin
      .from("candidate_medical_fact_evidence")
      .insert({
        user_id: intake.user_id,
        candidate_medical_fact_id: String(insertedCandidate.id),
        document_intake_id: intake.id,
        evidence_segment_id: candidate.evidenceSegmentId,
        evidence_kind: candidate.evidenceKind,
        page_number: candidate.pageNumber,
        cited_text: candidate.citedText,
        confidence_score: candidate.evidenceConfidence ?? candidate.extractionConfidence,
      });

    if (evidenceError) throw evidenceError;

    if (reviewStatus === "merged") {
      mergedCreatedCount += 1;
    } else {
      pendingCreatedCount += 1;
    }
  }

  return {
    pendingCreatedCount,
    mergedCreatedCount,
    conflictFlaggedCount,
    totalCount: existingRows.length + pendingCreatedCount + mergedCreatedCount,
  };
}

function countPdfPages(rawText: string): number | null {
  const pageMatches = rawText.match(/\/Type\s*\/Page\b/g);
  return pageMatches?.length ? pageMatches.length : null;
}

function latin1StringToBytes(value: string): Uint8Array {
  const bytes = new Uint8Array(value.length);
  for (let index = 0; index < value.length; index += 1) {
    bytes[index] = value.charCodeAt(index) & 0xff;
  }
  return bytes;
}

async function tryDecompressStream(bytes: Uint8Array): Promise<Uint8Array | null> {
  for (const format of ["deflate", "deflate-raw"]) {
    try {
      const blob = new Blob([bytes]);
      const stream = blob.stream().pipeThrough(new DecompressionStream(format as never));
      const buffer = await new Response(stream).arrayBuffer();
      return new Uint8Array(buffer);
    } catch {
      // Try the next format.
    }
  }

  return null;
}

function decodePdfLiteralString(literal: string): string {
  const source = literal.startsWith("(") && literal.endsWith(")")
    ? literal.slice(1, -1)
    : literal;

  let result = "";
  for (let index = 0; index < source.length; index += 1) {
    const char = source[index];

    if (char !== "\\") {
      result += char;
      continue;
    }

    const next = source[index + 1];
    if (!next) break;

    if (/[0-7]/.test(next)) {
      let octal = next;
      let consumed = 1;

      while (
        consumed < 3 &&
        index + 1 + consumed < source.length &&
        /[0-7]/.test(source[index + 1 + consumed])
      ) {
        octal += source[index + 1 + consumed];
        consumed += 1;
      }

      result += String.fromCharCode(parseInt(octal, 8));
      index += consumed;
      continue;
    }

    switch (next) {
      case "n":
        result += "\n";
        break;
      case "r":
        result += "\r";
        break;
      case "t":
        result += "\t";
        break;
      case "b":
        result += "\b";
        break;
      case "f":
        result += "\f";
        break;
      case "(":
      case ")":
      case "\\":
        result += next;
        break;
      case "\n":
      case "\r":
        break;
      default:
        result += next;
        break;
    }

    index += 1;
  }

  return result;
}

function collectPdfLiteralStrings(content: string): string[] {
  const decoded: string[] = [];
  let depth = 0;
  let current = "";
  let escaping = false;

  for (let index = 0; index < content.length; index += 1) {
    const char = content[index];

    if (depth === 0) {
      if (char === "(") {
        depth = 1;
        current = "(";
      }
      continue;
    }

    current += char;

    if (escaping) {
      escaping = false;
      continue;
    }

    if (char === "\\") {
      escaping = true;
      continue;
    }

    if (char === "(") {
      depth += 1;
      continue;
    }

    if (char === ")") {
      depth -= 1;

      if (depth === 0) {
        decoded.push(decodePdfLiteralString(current));
        current = "";
      }
    }
  }

  return decoded;
}

function extractPdfTextFromContent(content: string): string {
  const textBlocks = Array.from(content.matchAll(/BT([\s\S]*?)ET/g));
  const segments = textBlocks.length > 0
    ? textBlocks.map((match) => match[1])
    : [content];

  const strings = segments.flatMap((segment) => collectPdfLiteralStrings(segment));
  return normalizeExtractedText(strings.join("\n"));
}

async function extractPdfText(buffer: Uint8Array): Promise<ExtractionResult> {
  const rawPdfText = new TextDecoder("latin1").decode(buffer);
  const collectedTexts: string[] = [];

  const streamRegex = /<<([\s\S]*?)>>\s*stream\r?\n([\s\S]*?)\r?\nendstream/g;
  let streamMatch: RegExpExecArray | null;

  while ((streamMatch = streamRegex.exec(rawPdfText)) !== null) {
    const dictText = streamMatch[1];
    const streamText = streamMatch[2];
    let contentText = streamText;

    if (/\/FlateDecode\b/.test(dictText)) {
      const decompressed = await tryDecompressStream(latin1StringToBytes(streamText));
      if (decompressed) {
        contentText = new TextDecoder("latin1").decode(decompressed);
      }
    }

    const extracted = extractPdfTextFromContent(contentText);
    if (extracted) {
      collectedTexts.push(extracted);
    }
  }

  if (collectedTexts.length === 0) {
    const fallbackText = extractPdfTextFromContent(rawPdfText);
    if (fallbackText) {
      collectedTexts.push(fallbackText);
    }
  }

  const text = normalizeExtractedText(collectedTexts.join("\n\n"));
  if (!text) {
    throw new ExtractionFailure(
      "This PDF appears scanned, image-based, or encoded in a way the current extractor could not read. Manual review remains available until OCR support is added.",
      true,
    );
  }

  return {
    text,
    pageCount: countPdfPages(rawPdfText) ?? 1,
    extractorLabel: "pdf_text_heuristic",
    signalConfidence: 0.78,
  };
}

function decodeXmlEntities(value: string): string {
  return value
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, "\"")
    .replace(/&apos;/g, "'")
    .replace(/&#160;/g, " ")
    .replace(/&#x([0-9a-f]+);/gi, (_, hex) => String.fromCodePoint(parseInt(hex, 16)))
    .replace(/&#([0-9]+);/g, (_, decimal) => String.fromCodePoint(parseInt(decimal, 10)));
}

function convertDocxXmlToText(xml: string): string {
  const withBreaks = xml
    .replace(/<\/w:p>/g, "\n")
    .replace(/<w:tab\/>/g, "\t")
    .replace(/<w:br[^>]*\/>/g, "\n")
    .replace(/<\/w:tr>/g, "\n")
    .replace(/<\/w:tbl>/g, "\n\n");

  const withoutTags = withBreaks.replace(/<[^>]+>/g, " ");
  return normalizeExtractedText(decodeXmlEntities(withoutTags));
}

function readDocxPageCount(zipEntries: Record<string, Uint8Array>): number | null {
  const appXml = zipEntries["docProps/app.xml"];
  if (!appXml) return null;

  const xml = strFromU8(appXml);
  const pagesMatch = xml.match(/<Pages>(\d+)<\/Pages>/i);
  if (!pagesMatch) return null;

  const pages = Number(pagesMatch[1]);
  return Number.isFinite(pages) && pages > 0 ? pages : null;
}

async function extractDocxText(buffer: Uint8Array): Promise<ExtractionResult> {
  let zipEntries: Record<string, Uint8Array>;

  try {
    zipEntries = unzipSync(buffer);
  } catch {
    throw new ExtractionFailure(
      "The DOCX file could not be opened for text extraction. Manual review remains available.",
      true,
    );
  }

  const xmlPaths = Object.keys(zipEntries)
    .filter((path) =>
      /^word\/(document|header\d+|footer\d+|footnotes|endnotes)\.xml$/i.test(path)
    )
    .sort();

  const xmlTexts = xmlPaths
    .map((path) => convertDocxXmlToText(strFromU8(zipEntries[path])))
    .filter(Boolean);

  const text = normalizeExtractedText(xmlTexts.join("\n\n"));
  if (!text) {
    throw new ExtractionFailure(
      "The DOCX file did not contain readable text for the current extractor. Manual review remains available.",
      true,
    );
  }

  return {
    text,
    pageCount: readDocxPageCount(zipEntries) ?? 1,
    extractorLabel: "docx_xml",
    signalConfidence: 0.9,
  };
}

async function extractPlainText(blob: Blob): Promise<ExtractionResult> {
  const text = normalizeExtractedText(await blob.text());

  if (!text) {
    throw new ExtractionFailure(
      "The uploaded text document did not contain extractable text. Manual review remains available.",
      true,
    );
  }

  return {
    text,
    pageCount: 1,
    extractorLabel: "plain_text",
    signalConfidence: 0.98,
  };
}

function getUnsupportedFormatMessage(intake: MedicalDocumentIntakeRow): string {
  if (isImageIntake(intake)) {
    return "Image OCR is not enabled yet. Manual review remains available until OCR support is added.";
  }

  if (isLegacyDocIntake(intake)) {
    return "Legacy .doc extraction is not enabled yet. Please continue with manual review or re-export as DOCX/PDF when possible.";
  }

  return "Automatic extraction is not available for this file type yet. Manual review remains available.";
}

async function extractMedicalDocument(
  intake: MedicalDocumentIntakeRow,
  fileBlob: Blob
): Promise<ExtractionResult> {
  if (isTextIntake(intake)) {
    return extractPlainText(fileBlob);
  }

  const buffer = new Uint8Array(await fileBlob.arrayBuffer());

  if (isPdfIntake(intake)) {
    return extractPdfText(buffer);
  }

  if (isDocxIntake(intake)) {
    return extractDocxText(buffer);
  }

  throw new ExtractionFailure(getUnsupportedFormatMessage(intake), false);
}

Deno.serve(async (req: Request) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { status: 200, headers: corsHeaders });
  }

  if (req.method !== "POST") {
    return jsonResponse(405, {
      success: false,
      error: "Method not allowed",
    });
  }

  const supabaseUrl = Deno.env.get("SUPABASE_URL");
  const serviceRoleKey = Deno.env.get("SUPABASE_SERVICE_ROLE_KEY");

  if (!supabaseUrl || !serviceRoleKey) {
    return jsonResponse(500, {
      success: false,
      error: "Supabase edge function environment is not configured",
    });
  }

  const authHeader = req.headers.get("Authorization");
  const accessToken = authHeader?.replace(/^Bearer\s+/i, "").trim() ?? "";

  if (!accessToken) {
    return jsonResponse(401, {
      success: false,
      error: "Missing authorization token",
    });
  }

  const supabaseAdmin = createClient(supabaseUrl, serviceRoleKey, {
    auth: { persistSession: false, autoRefreshToken: false },
  });

  const {
    data: { user },
    error: authError,
  } = await supabaseAdmin.auth.getUser(accessToken);

  if (authError || !user) {
    return jsonResponse(401, {
      success: false,
      error: "Unable to authenticate document extraction request",
    });
  }

  let intakeId = "";
  try {
    const body = await req.json();
    intakeId = typeof body?.intake_id === "string" ? body.intake_id : "";
  } catch {
    return jsonResponse(400, {
      success: false,
      error: "Invalid JSON body",
    });
  }

  if (!intakeId) {
    return jsonResponse(400, {
      success: false,
      error: "Missing intake_id",
    });
  }

  const { data: intakeData, error: intakeError } = await supabaseAdmin
    .from("medical_document_intakes")
    .select("*")
    .eq("id", intakeId)
    .eq("user_id", user.id)
    .maybeSingle();

  if (intakeError) {
    return jsonResponse(500, {
      success: false,
      error: intakeError.message,
    });
  }

  if (!intakeData) {
    return jsonResponse(404, {
      success: false,
      error: "Document intake not found",
    });
  }

  const intake = intakeData as MedicalDocumentIntakeRow;

  const { data: existingSegmentRows, error: existingSegmentError } = await supabaseAdmin
    .from("medical_document_evidence_segments")
    .select("id", { count: "exact" })
    .eq("document_intake_id", intake.id)
    .eq("user_id", user.id)
    .like("extractor_label", `${AUTO_SEGMENT_LABEL_PREFIX}%`);

  if (existingSegmentError) {
    return jsonResponse(500, {
      success: false,
      error: existingSegmentError.message,
    });
  }

  const existingSegmentCount = existingSegmentRows?.length ?? 0;

  if (intake.extraction_status === "completed" && intake.extracted_text && existingSegmentCount === 0) {
    const backfillExtraction: ExtractionResult = {
      text: intake.extracted_text,
      pageCount: intake.page_count ?? 1,
      extractorLabel: "backfill_existing_text",
      signalConfidence: 0.88,
    };

    await persistEvidenceSegments(supabaseAdmin, intake, backfillExtraction);
    const evidenceSegments = await fetchAutoEvidenceSegments(supabaseAdmin, intake);
    const candidateSummary = await syncAutoGeneratedCandidates(
      supabaseAdmin,
      intake,
      evidenceSegments,
    );

    const refreshedIntake = await updateIntake(supabaseAdmin, intake.id, {
      candidate_count: candidateSummary.totalCount,
      intake_status: "review_ready",
      extraction_status: "completed",
    });

    return jsonResponse(200, {
      success: true,
      intake: refreshedIntake,
      extraction_supported: true,
      message:
        candidateSummary.pendingCreatedCount > 0 ||
        candidateSummary.mergedCreatedCount > 0 ||
        candidateSummary.conflictFlaggedCount > 0
          ? `Evidence segments were built from the existing extracted text. ${buildCandidateSummaryMessage(
              candidateSummary,
              "",
            )}`
          : "Evidence segments were built from the existing extracted text. No auto-generated candidates were found, so manual review remains available.",
    });
  }

  if (intake.extraction_status === "completed" && intake.extracted_text && existingSegmentCount > 0) {
    const evidenceSegments = await fetchAutoEvidenceSegments(supabaseAdmin, intake);
    const candidateSummary = await syncAutoGeneratedCandidates(
      supabaseAdmin,
      intake,
      evidenceSegments,
    );

    const refreshedIntake = await updateIntake(supabaseAdmin, intake.id, {
      candidate_count: candidateSummary.totalCount,
      intake_status: "review_ready",
      extraction_status: "completed",
    });

    return jsonResponse(200, {
      success: true,
      intake: refreshedIntake,
      extraction_supported: true,
      message:
        candidateSummary.pendingCreatedCount > 0 ||
        candidateSummary.mergedCreatedCount > 0 ||
        candidateSummary.conflictFlaggedCount > 0
          ? `Existing extracted evidence was refreshed. ${buildCandidateSummaryMessage(
              candidateSummary,
              "",
            )}`
          : candidateSummary.totalCount > 0
            ? "Document extraction and reviewable candidates are already available."
            : "Existing extracted evidence was refreshed, but no auto-generated candidates were found. Manual review remains available.",
    });
  }

  const processingIntake = await updateIntake(supabaseAdmin, intake.id, {
    intake_status: "processing",
    extraction_status: "processing",
    extraction_error: null,
  });

  if (!processingIntake.storage_bucket || !processingIntake.storage_path) {
    const failedIntake = await updateIntake(supabaseAdmin, intake.id, {
      intake_status: "review_ready",
      extraction_status: "failed",
      extraction_error:
        "This intake does not have a stored document artifact. Manual review remains available.",
    });

    return jsonResponse(200, {
      success: true,
      intake: failedIntake,
      extraction_supported: false,
      message: failedIntake.extraction_error,
    });
  }

  const { data: fileBlob, error: downloadError } = await supabaseAdmin.storage
    .from(processingIntake.storage_bucket)
    .download(processingIntake.storage_path);

  if (downloadError || !fileBlob) {
    const failedIntake = await updateIntake(supabaseAdmin, intake.id, {
      intake_status: "review_ready",
      extraction_status: "failed",
      extraction_error: `Failed to download the stored document for extraction. ${downloadError?.message ?? ""}`.trim(),
    });

    return jsonResponse(200, {
      success: true,
      intake: failedIntake,
      extraction_supported: true,
      message: failedIntake.extraction_error,
    });
  }

  try {
    const extraction = await extractMedicalDocument(processingIntake, fileBlob);
    await persistEvidenceSegments(supabaseAdmin, processingIntake, extraction);
    const evidenceSegments = await fetchAutoEvidenceSegments(supabaseAdmin, processingIntake);
    const candidateSummary = await syncAutoGeneratedCandidates(
      supabaseAdmin,
      processingIntake,
      evidenceSegments,
    );

    const completedIntake = await updateIntake(supabaseAdmin, intake.id, {
      intake_status: "review_ready",
      extraction_status: "completed",
      extraction_error: null,
      extracted_text: extraction.text,
      extracted_at: new Date().toISOString(),
      page_count: extraction.pageCount,
      candidate_count: candidateSummary.totalCount,
    });

    return jsonResponse(200, {
      success: true,
      intake: completedIntake,
      extraction_supported: true,
      message:
        candidateSummary.pendingCreatedCount > 0 ||
        candidateSummary.mergedCreatedCount > 0 ||
        candidateSummary.conflictFlaggedCount > 0
          ? `Document extraction completed and evidence segments are ready. ${buildCandidateSummaryMessage(
              candidateSummary,
              "",
            )}`
          : "Document extraction completed and evidence segments are ready for review. No auto-generated candidates were found, so manual review remains available.",
    });
  } catch (error) {
    const extractionSupported =
      error instanceof ExtractionFailure ? error.extractionSupported : true;
    const message =
      error instanceof Error
        ? error.message
        : "Document extraction failed. Manual review remains available.";

    const failedIntake = await updateIntake(supabaseAdmin, intake.id, {
      intake_status: "review_ready",
      extraction_status: "failed",
      extraction_error: message,
    });

    return jsonResponse(200, {
      success: true,
      intake: failedIntake,
      extraction_supported: extractionSupported,
      message,
    });
  }
});

