function splitMedicationLines(text: string): string[] {
  return normalizeStringList(
    text
      .split(/\n|;|•/g)
      .map((line) => line.trim())
      .filter((line) => line.length > 2),
  );
}

function deriveMedicationName(line: string, dosage: string | null): string {
  let candidate = line
    .replace(/^[•*\-\d.)\s]+/, "")
    .replace(/^(?:medications?|home meds?|discharge meds?|current meds?)\s*[:\-]?\s*/i, "")
    .replace(/^(?:take|takes|taking|continue|continued)\s+/i, "");

  if (dosage) {
    candidate = candidate.slice(0, candidate.toLowerCase().indexOf(dosage.toLowerCase())).trim();
  } else {
    const frequencyMatch = candidate.match(MEDICATION_FREQUENCY_REGEX);
    if (frequencyMatch?.index !== undefined) {
      candidate = candidate.slice(0, frequencyMatch.index).trim();
    }
  }

  candidate = candidate
    .replace(/\b(?:po|oral|tablet|tablets|capsule|capsules|cap|caps|tab|solution|suspension|inject(?:ion|able)?)\b.*$/i, "")
    .replace(/[,:;.\-]+$/, "")
    .trim();

  return toDisplayText(candidate);
}

function isPlausibleMedicationName(name: string): boolean {
  if (!name) return false;

  const tokens = name.split(/\s+/).filter(Boolean);
  if (tokens.length === 0 || tokens.length > 4) return false;

  return !tokens.every((token) => GENERIC_MEDICATION_STOP_WORDS.has(token.toLowerCase()));
}

function buildMedicationCandidatesFromSegment(
  segment: MedicalDocumentEvidenceSegmentRow
): AutoGeneratedCandidate[] {
  const text = segment.normalized_text || segment.quoted_text;
  const labelLower = segment.section_label?.toLowerCase() ?? "";
  const medicationContext =
    /medication|meds|prescription|pharmacy|rx/.test(labelLower) ||
    /\b(?:medications?|tablet|capsule|capsules|dose|dosage|mg|mcg|ml|prn|as needed|daily)\b/i
      .test(text);

  if (!medicationContext) {
    return [];
  }

  const lines = splitMedicationLines(text);
  const candidates: AutoGeneratedCandidate[] = [];

  for (const line of lines) {
    if (
      /\b(?:no current medications|none|nkda|allerg(?:y|ies)|diet|procedure|surgery)\b/i.test(line)
    ) {
      continue;
    }

    const dosageMatch = line.match(/\b\d+(?:\.\d+)?\s?(?:mg|mcg|g|ml|mL|units?|iu|IU)\b/);
    const frequencyMatch = line.match(MEDICATION_FREQUENCY_REGEX);
    if (!dosageMatch && !frequencyMatch && !/medication|meds|rx/.test(labelLower)) {
      continue;
    }

    const dosage = dosageMatch ? dosageMatch[0] : null;
    const medicationName = deriveMedicationName(line, dosage);
    if (!isPlausibleMedicationName(medicationName)) {
      continue;
    }

    const reasonMatch = line.match(/\b(?:for|reason)\b\s*[:\-]?\s*([^.;,\n]+)/i);
    const confidenceBase =
      dosage && /medication|meds|rx/.test(labelLower)
        ? 0.92
        : dosage
          ? 0.84
          : 0.76;

    candidates.push({
      category: "medication",
        detail: {
          medication_name: medicationName,
          dosage,
          frequency: frequencyMatch ? frequencyMatch[1].toLowerCase() : null,
          prescribing_reason: reasonMatch ? cleanListToken(reasonMatch[1]) : null,
        gi_side_effects_known: GI_RELEVANT_MEDICATION_HINTS.some((pattern) => pattern.test(line)),
        start_date: extractFirstDate(line),
          end_date: /\b(?:stopped|discontinued|ended|completed)\b/i.test(line)
            ? extractFirstDate(line)
            : null,
          is_current: !/\b(?:stopped|discontinued|ended|completed|former|previously)\b/i.test(line),
        },
        extractionConfidence: mergeCandidateConfidence(segment.confidence_score, confidenceBase),
      extractionNotes: buildAutoCandidateNote(
        segment.section_label,
        `Matched medication entry "${medicationName}"`
      ),
      evidenceKind: inferCandidateEvidenceKind("medication"),
      evidenceSegmentId: segment.id,
      pageNumber: segment.page_number,
      citedText: line,
      evidenceConfidence: segment.confidence_score,
    });
  }

  return candidates;
}

function buildCandidatesFromEvidenceSegment(
  segment: MedicalDocumentEvidenceSegmentRow
): AutoGeneratedCandidate[] {
  return [
    ...buildDiagnosisCandidatesFromSegment(segment),
    ...buildMedicationCandidatesFromSegment(segment),
    ...buildProcedureCandidatesFromSegment(segment),
    ...buildAllergyCandidatesFromSegment(segment),
    ...buildDietGuidanceCandidatesFromSegment(segment),
    ...buildRedFlagCandidatesFromSegment(segment),
  ];
}

